#include<stdio.h>
#include<math.h>

int arearectangle(int l,int b)
{
    return l*b;
}

int perimeterrectangle(int l,int b)
{
    return 2*(l+b);
}

int areasquare(int s)
{
    return s*s;
}


int perimetersquare(int s)
{
    return 4*s;
}

int arearhombus(int d1,int d2)
{
    return 0.5*d1*d2;
}

int perimeterrhombus(int s)
{
    return 4*s;
}